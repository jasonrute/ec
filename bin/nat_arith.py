# Primitives
def _incr(x): return lambda x: x + 1
def _rec(b):
    def _rec_inner(f):
        def _rec_inner2(n):
            val = b
            for i in range(n):
                if i >= 100:
                    raise RecursionDepthExceeded()
                val = f(i)(val)
            return val
        return _rec_inner2
    return _rec_inner

def of_seq(seq):
    return [{"i": i, "o": o} for i, o in enumerate(seq)]

def of_matrix(matrix):
    return [{"i": i, "j": j, "o": matrix[i][j]} for i in range(5) for j in range(5)]

def get_tint_seq_task(item):
    return Task(
        item["name"],
        arrow(tint, tint),
        [((ex["i"],), ex["o"]) for ex in item["examples"]],
    )

def get_tint_matrix_task(item):
    return Task(
        item["name"],
        arrow(tint, tint, tint),
        [((ex["i"],ex["j"]), ex["o"]) for ex in item["examples"]],
    )


